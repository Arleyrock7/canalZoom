



	




										
										        <h2>Términos y condiciones</h2>

										        <hr>
						
												<b>1-</b> Esta política de privacidad, que se incorpora por referencia a nuestros Términos de 

												Servicio, describe la recopilación y el uso más amplio posible de la información que podemos 

												obtener de usted ya sea en línea o fuera de línea, en relación con el uso de nuestros servicios.

												<br>
												<hr>

												<b>2-</b> Recopilación y uso de la información

												Al utilizar nuestras propiedades, podemos recopilar cierta información acerca de usted con su 

												consentimiento previo. Podemos recopilar, por ejemplo:<br><br>

												• Información de contacto, como su nombre, dirección de correo electrónico, dirección postal y 

												número de teléfono<br>

												• La edad u otras informaciones del perfil como rango de edad o fecha de nacimiento, 

												fotografías personales.<br>

												Podemos recibir y complementar la información que recopilamos sobre usted con información 

												de un tercero, siempre que ese tercero informe que ha recibido su consentimiento para 

												hacerlo. Esta información puede combinarse con información que ya tenemos sobre usted 

												para mejorar nuestra capacidad para servir a usted, adaptar nuestro contenido y ofertas a 

												usted.<br>

												Podemos utilizar esta información para:<br>

												• Administrar su cuenta<br>

												• Enviarle comunicaciones sobre ofertas producciones audiovisuales<br>

												• Supervisar y asegurar nuestros sitios y servicios<br>

												• Administrar concursos, sorteos y promociones que pueda haber ingresado, y poder enviar 

												notificaciones relacionadas.<br>

												• Enviar comunicaciones a sus amigos sobre su petición cuando usted proporciona su 

												información de contacto

												<br>
												<hr>

												<b>3-</b>Compartir información y divulgación

												Podemos compartir su información con nuestros afiliados, proveedores de servicios y otras 

												terceras partes, como se describe a continuación: <br><br>

												<b>• Afiliados:</b> Podemos compartir información entre nuestros afiliados explícitamente cubiertos 

												por esta póliza, así como cualquier otra empresa o filial. <br><br>

												<b>• Proveedores de Servicios:</b> Podemos proporcionar su información a personas o empresas 

												que conservamos para llevar a cabo o apoyar a nuestra operación de nuestras propiedades. 

												Estos proveedores de servicios están autorizados a utilizar su información sólo cuando sea 

												necesario para poder proveer estos servicios a nosotros. Por ejemplo, los proveedores de 

												servicios pueden: <br>

												- ayudarnos con nuestras actividades de negocios, tales como el cumplimiento de sus 

												peticiones, limpieza y verificación de las transacciones<br>

												- Gestión y mejora de los datos del cliente<br>

												- Proporcionar servicio al cliente<br>

												- La detección de fraude o actividades ilegales<br>

												- Realización de la investigación del cliente y encuestas<br><br>

												<b>• Terceros con fines de mercadeo:</b> Podemos compartir información agregada o anónima sobre 

												usted con terceros para fines de mercadeo, publicidad, investigación o similar. Con su 

												consentimiento, podremos compartir su información con un tercero no relacionado, que puede 

												ponerse en contacto con usted acerca de productos, servicios u otras ofertas que puedan ser 

												de su interés. Obtendremos su consentimiento en el lugar donde se recoge la información, ya 

												sea por métodos de "opt-out" (desvinculación) o "opt-in" (inscripción) o por otros medios (por 

												ejemplo una solicitud por correo electrónico). El uso de tercero y el tratamiento de su 

												información se regirán por las políticas de privacidad de dicho tercero. En el caso de que usted 

												tiene una pregunta acerca de la recolección y uso de su información de dicha parte, usted 

												debe ponerse en contacto con esa persona directamente. <br><br>

												<b>• Protección Jurídica y Cumplimiento de la Ley, transmisiones de empresas:</b> También 

												podemos divulgar la información en el contexto de un procedimiento judicial y en relación con 

												las actividades de aplicación de la ley, así como en el caso de venta de la empresa 

												correspondiente. Por ejemplo, podemos divulgar su información: <br><br>

												<b>-</b> conforme a la ley, para cumplir con una orden judicial o un proceso legal similar <br>

												<b>-</b> con las autoridades u organizaciones similares en el marco de una investigación criminal o 

												prevención del fraude o actividad criminal, violación de derechos de propiedad intelectual u 

												otra actividad que se sospecha que es ilegal o que pueda exponer a nuestros usuarios o 

												nosotros a la responsabilidad legal <br>

												<b>-</b> utilizar su información y registros para la defensa de una demanda, investigación o acción si 

												tal información, registros o perfiles son relevantes a la demanda, la protección o la acción <br>

												<b>-</b> cuando creemos de buena fe que dicha divulgación es necesaria para proteger nuestros 

												derechos, la protección de su seguridad o la seguridad de otros, investigar el fraude, o para 

												responder a una solicitud del gobierno

												<br>
												<hr>

												<b>4-</b> Acceso de usuario y cambios a su cuenta

												Si cambia su información, o si ya no desea nuestro servicio, usted puede solicitar corregir, 

												actualizar, desactivar o borrar/eliminar su información, únicamente poniéndose en contacto 

												con nosotros. Podremos rechazar la tramitación de solicitudes que consideremos 

												irrazonablemente repetitivas, requieran un esfuerzo técnico desproporcionado, comprometan 

												la privacidad de los demás, sean muy poco prácticas, o para los que el acceso no está 

												obligado por la ley. <br><br>

												Al optar por desactivar o borrar/eliminar su información o solicitar cualquier otro cambio que 

												pueda afectar negativamente a la entrega de nuestros productos y servicios, es posible que ya 

												no recibir información de nuestra parte o seguir disfrutando del nivel de servicio que 

												previamente ha estado proporcionando a usted o, simplemente, puede que no seamos 

												capaces de ofrecerle nuestros servicios.

												<br>
												<hr>

												<b>5-</b> Niños

												Todas las personas interesadas en contar con nuestros servicios deben ser mayores de edad, 

												en caso en que el interesado sea menor de edad debe contar con la autorización del 

												acudiente, un adulto responsable y/o representante legal del mismo, adicional a esto el 

												registro debe hacerse de forma presencial en la empresa con ayuda de un coordinador de la 

												agencia de casting. <br><br>

												Si nos damos cuenta de que hemos recogido información de un niño sin el consentimiento de 

												un padre o tutor, haremos todos los esfuerzos razonables para eliminar la información de 

												nuestros archivos;

												<br>
												<hr>

												<b>6-</b> Seguridad, retención y supervisión de información

												Utilizamos los esfuerzos comercialmente razonables para implementar las medidas 

												apropiadas para proteger su información contra la divulgación no autorizada o accidental, el 

												acceso, el uso indebido, la pérdida o alteración. Aunque nos esforzamos por proteger su 

												información, no podemos asegurar o garantizar la seguridad de dicha información. Podemos 

												controlar su uso de nuestros servicios para diversos fines, tales como la protección de 

												nuestros derechos, la protección de los derechos de los demás, o investigar cualquier 

												amenaza potencial a nuestra red. <br><br>

												Retención de Información y disposición (eliminación) Podemos conservar la información sobre 

												usted, siempre y cuando sea necesario para que cumplamos con los fines descritos en esta 

												política. Además, conservar la información durante un período adicional que se permita o 

												requiera para cumplir con nuestras obligaciones legales, resolver disputas y hacer cumplir los 

												acuerdos. Vamos a borrar su información de una manera irreproducible cuando la información 

												ya no es necesaria o requerida para ello. Incluso si borramos su información, la información 

												puede permanecer en los medios de copia de seguridad o de archivo, así como otros sistemas 

												de información.

												<br>
												<hr>

												<b>7-</b> Otra información

												Cambios en la Política Nos reservamos el derecho de realizar cambios en esta política de 

												privacidad en cualquier momento. Si hacemos un cambio importante en esta política de 

												privacidad, le notificaremos publicando un aviso del cambio en nuestras propiedades, según 

												sea pertinente o requerido por la ley. Nosotros publicaremos los cambios en la política de 

												privacidad y, si es necesario, le dará opciones adicionales con respecto a los cambios. <br><br> Por 

												favor revise esta política de privacidad de vez en cuando para asegurarse de que usted está 

												enterado de cualquier cambio. El uso continuado de nuestras propiedades después de 

												cualquier cambio o revisión de esta política de privacidad significará su aceptación y acuerdo 

												con estos cambios y revisiones.

		




																
												